# Como se representan los datos 
# - Los datos se clasifican en Tipos.
# - Son los diferentes dominios existentes.
# Ejemplo: 
# --> Edad, años de nacimiento, numero de multas.
#   --> Tienen dominio numerico.
# --> Nombre, Direccion, Nu.Celula.
# Tipos de datos basicos:
#   --> Enteros.
#   --> Reales.
#   --> Logicos.
#   --> Strings.
# En la computadora:
#  --> Cada byte es un casillero y tiene una direccion en memoria.
# Los datos (numeros y letras) se almacena en estos casilleros.
# ¿Cuantos casilleros ocupa un dato?
#   --> Depende de su tipo y del hardaware de la computadora.
#   --> Un entero puede ocupar casillas de hasta 4 bytes.
#   --> Un doble siempre ocupara mas, por su mayor precisión.
# Pero, un caracter siempre ocupara casillas de 1 byte.
